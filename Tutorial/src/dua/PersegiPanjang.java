package dua;

public class PersegiPanjang {
    
    private int panjang;
    private int lebar;
    
    public PersegiPanjang() {
        this.panjang = 5;
        this.lebar = 5;
    }
    
    public PersegiPanjang(int panjang, int lebar) {
        this.panjang = panjang;
        this.lebar = lebar;
    }
    
    public PersegiPanjang(int panjang) {
        this.panjang = panjang;
        this.lebar = 5;
    }
    
}
